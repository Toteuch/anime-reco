package com.toteuch.anime.reco.data.api.response.animedetails;

public class GenreResponse {
    private Long id;
    private String name;

    public GenreResponse() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
